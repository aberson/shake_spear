def test_subprocess_recap_is_surgical(workshop: Path, story: Path) -> None:
    _author_active_state(story)
    result = subprocess.run(
        [sys.executable, "-m", "shake_spear", "recap", SLUG],
        cwd=workshop,
        capture_output=True,
        text=True,
        check=False,
    )
    assert result.returncode == 0, result.stderr
    assert result.stdout.strip().splitlines() == [str(story / "active_state.md")]
    raw = (story / "active_state.md").read_bytes()
    assert raw.startswith(PREFIX + START), "subprocess run must preserve the prefix bytes"
    assert raw.endswith(END + SUFFIX), "subprocess run must preserve the suffix bytes"
    assert b"OLD RECAP CONTENT" not in raw
    assert b"Recap generated by" in raw
