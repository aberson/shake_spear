# THE load-bearing test: surgical marker-block rewrite (plan §3.4)
# ---------------------------------------------------------------------------


def test_recap_rewrites_only_the_marker_block(
    story: Path, frozen_date: str, capsys: pytest.CaptureFixture[str]
) -> None:
    _author_active_state(story)
    capsys.readouterr()  # drop story-fixture output
    assert main(["recap", SLUG]) == 0
    path = story / "active_state.md"
    assert capsys.readouterr().out.strip().splitlines() == [str(path)]
    raw = path.read_bytes()
    assert raw.startswith(PREFIX + START), "bytes before/through the start marker changed"
    assert raw.endswith(END + SUFFIX), "bytes from the end marker to EOF changed"
    block = raw[len(PREFIX + START) : len(raw) - len(END + SUFFIX)].decode("utf-8")
    assert "OLD RECAP CONTENT" not in block, "the between-markers text must be replaced"
    assert "ss:recap:" not in block, (
        "the new block must never nest marker lines (recap reading its own old output)"
    )
    assert f"_Recap generated by `ss recap` on {DATE}." in block, "dated first line"
    assert "**Current state:** Drafting chapter two; Nova just found the oven." in block
    assert "- Who lit the pilot light?" in block
    assert "- Why does the dough sing?" in block
    assert "Write the first two lines of the kitchen argument." in block


def test_generated_block_is_lf_only(story: Path, frozen_date: str) -> None:
    _author_active_state(story)
    assert main(["recap", SLUG]) == 0
    raw = (story / "active_state.md").read_bytes()
    between = raw[raw.find(START) + len(START) : raw.find(END)]
    assert b"\r" not in between, "the generated block itself must be LF-only"


def test_recap_twice_same_day_is_idempotent(story: Path, frozen_date: str) -> None:
    _author_active_state(story)
    assert main(["recap", SLUG]) == 0
    first = (story / "active_state.md").read_bytes()
    assert main(["recap", SLUG]) == 0
    assert (story / "active_state.md").read_bytes() == first, (
        "a second same-day recap must leave the file byte-identical"
    )


def test_recap_leaves_no_temp_residue(story: Path, frozen_date: str) -> None:
    """The atomic sibling-temp + os.replace write must clean up after itself."""
    _author_active_state(story)
    assert main(["recap", SLUG]) == 0
    residue = [path.name for path in story.iterdir() if ".tmp" in path.name]
    assert residue == [], f"recap left temp files behind: {residue}"


