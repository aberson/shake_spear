def _file_snapshot(story: Path) -> dict[Path, bytes]:
    return {path: path.read_bytes() for path in sorted(story.rglob("*")) if path.is_file()}


def test_status_prints_fields_and_newest_session_writes_nothing(
    story: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    _author_active_state(story)
    _write_log(story, "2026-07-01_scene_45min.md")
    _write_log(story, "2026-07-02_scene_45min.md")
    before = _file_snapshot(story)
    capsys.readouterr()
    assert main(["status", SLUG]) == 0
    out = capsys.readouterr().out
    assert "Current status: Drafting chapter two; Nova just found the oven." in out
    assert "Current scene or chapter: (not set)" in out, "missing section falls back"
    assert "Open loops:" in out
    assert "- Who lit the pilot light?" in out
    assert "Next tiny action: Write the first two lines of the kitchen argument." in out
    assert "Newest session: 2026-07-02_scene_45min.md" in out
    assert _file_snapshot(story) == before, "status must write nothing"


def test_status_fresh_story_all_not_set(story: Path, capsys: pytest.CaptureFixture[str]) -> None:
    capsys.readouterr()
    assert main(["status", SLUG]) == 0
    out = capsys.readouterr().out
    for label in session.STATUS_SECTIONS:
        assert f"{label}: (not set)" in out
    assert "Newest session: (none)" in out


