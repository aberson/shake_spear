#: Operator prose that mentions the marker strings without authoring marker
#: LINES: inline in a sentence, alone-on-a-line but backticked, and inside a
#: code fence with surrounding text. None of these may latch the surgery.
PROSE_MENTIONS = (
    b"## Notes for AI assistants\r\n"
    b"\r\n"
    b"The text between <!-- ss:recap:start --> and <!-- ss:recap:end --> is generated.\r\n"
    b"`<!-- ss:recap:start -->`\r\n"
    b"```\r\n"
    b"see <!-- ss:recap:start --> in active_state.md\r\n"
    b"```\r\n"
    b"\r\n"
)


def test_prose_marker_mentions_above_real_block_are_untouched(
    story: Path, frozen_date: str
) -> None:
    """Marker strings quoted in prose/fences above a REAL marker block: the
    surgery must replace the real block and leave the prose byte-identical."""
    (story / "active_state.md").write_bytes(
        PREFIX + PROSE_MENTIONS + START + b"\nOLD RECAP CONTENT\n" + END + SUFFIX
    )
    assert main(["recap", SLUG]) == 0
    raw = (story / "active_state.md").read_bytes()
    assert raw.startswith(PREFIX + PROSE_MENTIONS + START), (
        "prose mentions above the real block must survive byte-identically"
    )
    assert raw.endswith(END + SUFFIX), "bytes from the real end marker to EOF changed"
    assert b"OLD RECAP CONTENT" not in raw, "the real block must be the one replaced"
    assert b"Recap generated by" in raw


def test_prose_only_marker_mention_appends_new_block(story: Path, frozen_date: str) -> None:
    """An inline mention is NOT a marker line — with no real block anywhere,
    recap takes the append path and the prose survives byte-identically."""
    original = PREFIX + b"See <!-- ss:recap:start --> and <!-- ss:recap:end --> in the docs.\r\n"
    (story / "active_state.md").write_bytes(original)
    assert main(["recap", SLUG]) == 0
    raw = (story / "active_state.md").read_bytes()
    assert raw.startswith(original), "the prose mention must survive byte-identically"
    appended = raw[len(original) :]
    assert appended.startswith(b"\n" + START + b"\n")
    assert appended.endswith(b"\n" + END + b"\n")
    assert b"Recap generated by" in appended


def test_multiple_marker_pairs_exit_1_file_unchanged(
    story: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    """Two complete marker blocks: recap must refuse (which one is THE block?)."""
    original = (
        PREFIX
        + START
        + b"\nfirst block\n"
        + END
        + b"\n\r\noperator prose between blocks\r\n\r\n"
        + START
        + b"\nsecond block\n"
        + END
        + SUFFIX
    )
    (story / "active_state.md").write_bytes(original)
    capsys.readouterr()
    assert main(["recap", SLUG]) == 1
    assert "multiple recap marker blocks found - remove the duplicates" in capsys.readouterr().err
    assert (story / "active_state.md").read_bytes() == original, (
        "a duplicated-block file must not be touched"
    )


# ---------------------------------------------------------------------------
# markers absent -> append at EOF; corrupt marker state -> exit 1, untouched
# ---------------------------------------------------------------------------


def test_markers_absent_appends_block_at_eof(story: Path, frozen_date: str) -> None:
    original = PREFIX + b"## Notes for AI assistants\r\nKeep it cozy. \t\r\n"
    (story / "active_state.md").write_bytes(original)
    assert main(["recap", SLUG]) == 0
    raw = (story / "active_state.md").read_bytes()
    assert raw.startswith(original), "original content must be a byte-identical prefix"
    appended = raw[len(original) :]
    assert appended.startswith(b"\n" + START + b"\n")
    assert appended.endswith(b"\n" + END + b"\n")
    assert raw.count(START) == 1 and raw.count(END) == 1, "markers now present exactly once"
    assert b"Recap generated by" in appended


