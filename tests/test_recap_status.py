#: A >80-char next tiny action — the recap bullet must truncate it with "...".
LONG_ACTION = (
    "Finish the kitchen argument scene where Nova finally admits she fed the "
    "sourdough starter to the gravity oven on purpose"
)


def test_recap_lists_exactly_three_latest_sessions(story: Path, frozen_date: str) -> None:
    """4 logs across 2 dates -> the 3 newest by filename sort desc, with snippets."""
    assert len(LONG_ACTION) > 80, "fixture must exceed the 80-char snippet cap"
    _write_log(story, "2026-07-01_scene_45min.md", "## Session goal\n\nOld goal line.\n")
    _write_log(story, "2026-07-01_scene_45min_b.md")
    _write_log(
        story,
        "2026-07-02_scene_45min.md",
        f"## Session goal\n\nGoal line.\n\n## Next tiny action\n\n{LONG_ACTION}\n",
    )
    _write_log(story, "2026-07-02_freewrite_15min.md", "## Session goal\n\nMorning pages.\n")
    assert main(["recap", SLUG]) == 0
    block = _block_text(story)
    assert f"- `2026-07-02_scene_45min.md` — {LONG_ACTION[:77]}..." in block, (
        "Next tiny action is preferred over Session goal; >80 chars truncates to 77 + '...'"
    )
    assert LONG_ACTION not in block, "the full >80-char line must not appear untruncated"
    assert "- `2026-07-02_freewrite_15min.md` — Morning pages." in block, (
        "Session goal is the fallback snippet"
    )
    assert "- `2026-07-01_scene_45min_b.md`" in block, "sectionless log lists as bare filename"
    assert "2026-07-01_scene_45min.md" not in block, "the 4th-newest log must not be listed"
    assert block.count("\n- `") == 3, "exactly 3 session bullets"


