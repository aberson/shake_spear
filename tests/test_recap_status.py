"""Integration tests for ``ss recap`` / ``ss status`` (plan §11 Step 10).

THE load-bearing guarantee (plan §3.4): recap rewrites ONLY the text between
the ``ss:recap`` markers in ``active_state.md`` — every byte outside them
(operator CRLF lines, trailing whitespace, tabs) survives verbatim. All tests
drive the production CLI — ``cli.main()`` in-process plus one true
``python -m shake_spear`` subprocess — against the shared conftest fixtures
(real templates/skeleton, real ``new-story`` run).
"""

from __future__ import annotations

import subprocess
import sys
from pathlib import Path

import pytest

from conftest import SLUG
from shake_spear import session
from shake_spear.cli import main

DATE = "2026-07-02"

#: Literal marker lines (plan Appendix B) — NOT imported from session.py, so
#: drift in the shipped constants fails against the plan, not against itself.
START = b"<!-- ss:recap:start -->"
END = b"<!-- ss:recap:end -->"

#: Operator-authored bytes BEFORE the marker block: a UTF-8 BOM, mixed CRLF
#: and LF lines, plus a trailing-whitespace quirk — recap must preserve all
#: of it verbatim.
PREFIX = (
    b"\xef\xbb\xbf# Active State\r\n"
    b"\r\n"
    b"## Current status\n"
    b"\n"
    b"Drafting chapter two; Nova just found the oven.   \n"
    b"\n"
    b"## Open loops\n"
    b"\n"
    b"- Who lit the pilot light?\n"
    b"- Why does the dough sing?\n"
    b"\n"
    b"## Next tiny action\n"
    b"\n"
    b"Write the first two lines of the kitchen argument.\n"
    b"\n"
)

#: Operator-authored bytes AFTER the marker block: CRLF, a tab, trailing
#: spaces, and a whitespace-only final line.
SUFFIX = b"\r\n\r\n## Notes for AI assistants\r\nKeep it cozy. \t\r\n   \r\n"


@pytest.fixture()
def frozen_date(monkeypatch: pytest.MonkeyPatch) -> str:
    """Pin ``session._today`` so the dated recap first line is deterministic."""
    monkeypatch.setattr(session, "_today", lambda: DATE)
    return DATE


def _author_active_state(story: Path) -> bytes:
    """Overwrite active_state.md with the quirk-laden operator file; return its bytes."""
    raw = PREFIX + START + b"\nOLD RECAP CONTENT\n" + END + SUFFIX
    (story / "active_state.md").write_bytes(raw)
    return raw


def _block_text(story: Path) -> str:
    """The text between the markers in the story's active_state.md."""
    text = (story / "active_state.md").read_text(encoding="utf-8")
    return text.split(START.decode(), 1)[1].split(END.decode(), 1)[0]


def _write_log(story: Path, name: str, body: str = "# Writing Session\n") -> None:
    """Drop a session log with a chosen filename (dates vary across logs)."""
    (story / "sessions" / name).write_text(body, encoding="utf-8")


