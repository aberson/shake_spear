def test_recap_counts_entities_and_summarizes_continuity(
    story: Path, frozen_date: str, capsys: pytest.CaptureFixture[str]
) -> None:
    capsys.readouterr()
    assert main(["scene", SLUG, "Oven Door"]) == 0
    assert main(["scene", SLUG, "Kitchen Argument"]) == 0
    assert main(["character", SLUG, "Nova"]) == 0
    (story / "continuity.md").write_text(
        "---\ntype: continuity\nstatus: active\n---\n\n# Continuity Log\n\n"
        "## Timeline events\n\n- Day 1: oven found.\n- Day 2: dough sings.\n\n"
        "## Character facts\n\n- Nova is twelve.\n",
        encoding="utf-8",
    )
    assert main(["recap", SLUG]) == 0
    block = _block_text(story)
    assert "**Story so far:** 2 scenes, 1 character, 0 world elements, 0 drafts" in block
    assert (
        "**Continuity notes:** 3 note lines in continuity.md "
        "under Timeline events, Character facts" in block
    )
    # count==1 uses the singular noun (re-run recap after shrinking the log).
    (story / "continuity.md").write_text(
        "# Continuity Log\n\n## Character facts\n\n- Nova is twelve.\n",
        encoding="utf-8",
    )
    assert main(["recap", SLUG]) == 0
    block = _block_text(story)
    assert "**Continuity notes:** 1 note line in continuity.md under Character facts" in block, (
        "a single note must render as '1 note line', not '1 note lines'"
    )


