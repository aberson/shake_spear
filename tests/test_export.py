"""Integration tests for ``ss export`` (plan §11 Step 11).

All tests go through the production CLI entry point — ``cli.main()``
in-process plus a true ``python -m shake_spear`` subprocess — against the
shared ``workshop``/``story`` fixtures (real templates/skills/skeleton, real
``new-story`` run). Drafts are hand-written files: ``drafts/`` has no creator
command by design (plan §3.4 — the operator's prose is never generated).
"""

from __future__ import annotations

import re
import subprocess
import sys
from pathlib import Path

import pytest

from conftest import SLUG
from shake_spear.cli import main
from shake_spear.indexer import _draft_files

DERIVED_NOTE = "_(Derived file: regenerated by `ss export`; safe to overwrite.)_"


def manuscript_path(story: Path) -> Path:
    return story / "exports" / "manuscript.md"


def manuscript_text(story: Path) -> str:
    return manuscript_path(story).read_text(encoding="utf-8")


def write_draft(story: Path, name: str, content: str) -> Path:
    draft = story / "drafts" / name
    draft.write_text(content, encoding="utf-8")
    return draft


# ---------------------------------------------------------------------------
# ordering: lexicographic by filename, files only (plan §4 export row)
# ---------------------------------------------------------------------------


def test_parts_in_lexicographic_filename_order(story: Path) -> None:
    # Created deliberately OUT of lexicographic order. The unpadded 9_w
    # sorting AFTER 10_z ("1" < "9" stringwise) disambiguates lexicographic
    # from natural/numeric sort, and epilogue proves a prefix-less name
    # slots in lexicographically too.
    write_draft(story, "10_z.md", "Part ten.\n")
    write_draft(story, "02_y.md", "Part two.\n")
    write_draft(story, "epilogue.md", "The end.\n")
    write_draft(story, "9_w.md", "Part nine.\n")
    write_draft(story, "01_x.md", "Part one.\n")
    assert main(["export", SLUG]) == 0
    headings = [line for line in manuscript_text(story).splitlines() if line.startswith("## ")]
    assert headings == ["## 01_x.md", "## 02_y.md", "## 10_z.md", "## 9_w.md", "## epilogue.md"]


def test_only_files_and_only_toplevel_md_are_gathered(story: Path) -> None:
    write_draft(story, "01_x.md", "Real part.\n")
    (story / "drafts" / "folder.md").mkdir()  # a DIRECTORY matching the glob
    nested = story / "drafts" / "sub"
    nested.mkdir()
    (nested / "nested.md").write_text("Nested note.\n", encoding="utf-8")
    (story / "drafts" / "notes.txt").write_text("Not markdown.\n", encoding="utf-8")
    assert main(["export", SLUG]) == 0
    text = manuscript_text(story)
    assert "1 part.\n" in text
    assert "## 01_x.md" in text
    for absent in ("folder.md", "nested.md", "notes.txt", "Nested note", "Not markdown"):
        assert absent not in text


