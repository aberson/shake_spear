# ---------------------------------------------------------------------------
# true subprocess test through python -m shake_spear
# ---------------------------------------------------------------------------


def test_subprocess_export(workshop: Path, story: Path) -> None:
    write_draft(story, "01_x.md", "Part one.\n")
    result = subprocess.run(
        [sys.executable, "-m", "shake_spear", "export", SLUG],
        cwd=workshop,
        capture_output=True,
        text=True,
        check=False,
    )
    assert result.returncode == 0, result.stderr
    assert result.stdout.strip() == str(manuscript_path(story))
    assert "## 01_x.md" in manuscript_text(story)
