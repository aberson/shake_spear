# ---------------------------------------------------------------------------
# --out guards (iter 2): drafts/ rejection, refuse/backup for existing
# targets, self-concatenation exclusion, clean error for unwritable targets
# ---------------------------------------------------------------------------


def test_out_pointing_at_existing_draft_rejected_exit_1(
    story: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    draft = write_draft(story, "01_x.md", "Operator prose - never clobber.\n")
    before = draft.read_bytes()
    assert main(["export", SLUG, "--out", str(draft)]) == 1
    assert "--out may not point inside drafts/" in capsys.readouterr().err
    assert draft.read_bytes() == before, "the draft must be untouched"
    # --force must NOT bypass the drafts/ rejection either.
    assert main(["export", SLUG, "--out", str(draft), "--force"]) == 1
    assert draft.read_bytes() == before


def test_out_new_or_nested_name_inside_drafts_also_rejected(
    story: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    write_draft(story, "01_x.md", "Part one.\n")
    for rel in ("new_name.md", "sub/deep.md"):
        target = story / "drafts" / rel
        assert main(["export", SLUG, "--out", str(target)]) == 1
        assert "--out may not point inside drafts/" in capsys.readouterr().err
        assert not target.exists()


def test_out_existing_file_refused_exit_2_without_force(
    workshop: Path, story: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    write_draft(story, "01_x.md", "Part one.\n")
    precious = workshop / "precious.md"
    precious.write_text("Operator prose - do not lose.\n", encoding="utf-8")
    assert main(["export", SLUG, "--out", str(precious)]) == 2
    assert "--force" in capsys.readouterr().err
    assert precious.read_text(encoding="utf-8") == "Operator prose - do not lose.\n"
    assert list(workshop.glob("*.bak-*")) == []


def test_out_existing_file_force_backs_up_overwrites_and_prints_backup(
    workshop: Path, story: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    write_draft(story, "01_x.md", "Part one.\n")
    precious = workshop / "precious.md"
    precious.write_text("Old operator prose.\n", encoding="utf-8")
    assert main(["export", SLUG, "--out", str(precious), "--force"]) == 0
    out = capsys.readouterr().out
    backups = list(workshop.glob("precious.md.bak-*"))
    assert len(backups) == 1
    assert backups[0].read_text(encoding="utf-8") == "Old operator prose.\n"
    assert str(precious) in out
    assert str(backups[0]) in out
    assert "## 01_x.md" in precious.read_text(encoding="utf-8")


def test_out_spelling_the_default_path_keeps_derived_semantics(story: Path) -> None:
    write_draft(story, "01_x.md", "Part one.\n")
    assert main(["export", SLUG]) == 0  # the default manuscript now exists
    # Re-export --out pointing AT the default: still derived — no --force
    # needed (exit 0, not 2), no .bak- backup created.
    assert main(["export", SLUG, "--out", str(manuscript_path(story))]) == 0
    assert list(story.rglob("*.bak-*")) == []


def test_draft_set_excludes_resolved_export_target(story: Path) -> None:
    # Defense in depth: through the CLI the drafts/-tree rejection fires
    # first, so the exclusion only matters for aliased paths (e.g. a
    # symlinked draft resolving to an outside-drafts/ target, which needs
    # privileges to create on Windows) — assert the helper contract directly.
    first = write_draft(story, "01_x.md", "Part one.\n")
    write_draft(story, "02_y.md", "Part two.\n")
    assert [p.name for p in _draft_files(story)] == ["01_x.md", "02_y.md"]
    assert [p.name for p in _draft_files(story, exclude=first.resolve())] == ["02_y.md"]


