def test_crlf_draft_yields_cr_free_manuscript(story: Path) -> None:
    (story / "drafts" / "windows.md").write_bytes(
        b"---\r\ntitle: Windows Draft\r\n---\r\nLine one from CRLF land.\r\nLine two.\r\n"
    )
    assert main(["export", SLUG]) == 0
    raw = manuscript_path(story).read_bytes()
    assert b"\r" not in raw
    assert raw[:1] != b"\xef", "no BOM"
    assert "Line one from CRLF land.\nLine two." in manuscript_text(story)


