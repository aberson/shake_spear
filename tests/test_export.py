# ---------------------------------------------------------------------------
# drafts strictly read-only (plan §3.4): bytes AND mtimes unchanged
# ---------------------------------------------------------------------------


def test_drafts_byte_identical_and_mtime_unchanged(story: Path) -> None:
    drafts = [
        write_draft(story, "01_x.md", "---\nstatus: raw\n---\nPart one.\n"),
        write_draft(story, "02_y.md", "Part two.\n"),
    ]
    before = [(d.read_bytes(), d.stat().st_mtime_ns) for d in drafts]
    assert main(["export", SLUG]) == 0
    after = [(d.read_bytes(), d.stat().st_mtime_ns) for d in drafts]
    assert after == before


# ---------------------------------------------------------------------------
# derived-artifact semantics: overwrite freely, no .bak, idempotent (plan §3.4)
# ---------------------------------------------------------------------------


def test_second_run_overwrites_without_bak_and_is_byte_identical(story: Path) -> None:
    write_draft(story, "01_x.md", "Part one.\n")
    assert main(["export", SLUG]) == 0  # NOT exit 2 - the default target needs no --force
    first = manuscript_path(story).read_bytes()
    assert main(["export", SLUG]) == 0
    assert manuscript_path(story).read_bytes() == first
    assert list(story.rglob("*.bak-*")) == [], "derived overwrite must not create backups"
    # Guard the no-timestamp invariant (same rationale as index.md's).
    assert not re.search(r"\b\d{2}:\d{2}(:\d{2})?\b", first.decode("utf-8"))


def test_export_reflects_draft_edits_on_regeneration(story: Path) -> None:
    draft = write_draft(story, "01_x.md", "Old prose.\n")
    assert main(["export", SLUG]) == 0
    draft.write_text("New prose.\n", encoding="utf-8")
    assert main(["export", SLUG]) == 0
    text = manuscript_text(story)
    assert "New prose." in text
    assert "Old prose." not in text


# ---------------------------------------------------------------------------
